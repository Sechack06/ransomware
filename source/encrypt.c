void addRoundKey(char block[4][4], char roundkey[4][4]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++)
            block[i][j] ^= roundkey[i][j];
    }
}

void SubBytes(char block[4][4]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++)
            block[i][j] = sbox[(block[i][j] & 0xf0) >> 4][block[i][j] & 0xf];
    }
}

void ShiftRows(char block[4][4]) {
    char prev[4][4];
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++)
            prev[i][j] = block[i][j];
    }
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++)
            block[i][j] = prev[i][(j + i) % 4];
    }
}

char xtime(char x)
{
    return ((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

void MixColumns(char block[4][4])
{
    char Tmp, Tm, t;
    for (int i = 0; i < 4; ++i)
    {
        t = block[i][0];
        Tmp = block[i][0] ^ block[i][1] ^ block[i][2] ^ block[i][3];
        Tm = block[i][0] ^ block[i][1]; Tm = xtime(Tm);  block[i][0] ^= Tm ^ Tmp;
        Tm = block[i][1] ^ block[i][2]; Tm = xtime(Tm);  block[i][1] ^= Tm ^ Tmp;
        Tm = block[i][2] ^ block[i][3]; Tm = xtime(Tm);  block[i][2] ^= Tm ^ Tmp;
        Tm = block[i][3] ^ t; Tm = xtime(Tm);  block[i][3] ^= Tm ^ Tmp;
    }
}

void cbc(char block[4][4], char prev[4][4]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++)
            block[i][j] ^= prev[i][j];
    }
}

void RotWord(char w[4]) {
    char tmp = w[0];
    w[0] = w[1];
    w[1] = w[2];
    w[2] = w[3];
    w[3] = tmp;
}

void SubWord(char w[4]) {
    for (int i = 0; i < 4; i++)
        w[i] = sbox[(w[i] & 0xf0) >> 4][w[i] & 0xf];
}

void KeyExpansion(char expandedKey[44][4], char* key) {
    char R[10] = { 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36 };

    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++)
            expandedKey[i][j] = key[i * 4 + j];
    }

    char w[4];
    w[0] = expandedKey[3][0];
    w[1] = expandedKey[3][1];
    w[2] = expandedKey[3][2];
    w[3] = expandedKey[3][3];

    for (int i = 4; i < 44; i++) {
        RotWord(w);
        SubWord(w);
        w[0] ^= R[i / 4 - 1];
        for (int j = 0; j < 4; j++) {
            w[j] ^= expandedKey[i - 4][j];
            expandedKey[i][j] = w[j];
        }
    }
}

void padding(char* pt, int ptlen) {
    int padnum = 16 - (ptlen % 16);
    for (int i = 0; i < padnum; i++)
        pt[ptlen + i] = (char)padnum;
}

int encrypt(char* key, char* pt, char* enc, int ptlen) {
    char prev[4][4];
    char expandedKey[44][4];
    char* padpt = malloc(ptlen + 16);
    memcpy(padpt, pt, ptlen);
    padding(padpt, ptlen);
    KeyExpansion(expandedKey, key);

    char keym[4][4];
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++)
            keym[i][j] = key[i * 4 + j];
    }
    for (int i = 0; i < ptlen / BLOCK_SIZE + 1; i++) {
        char block[4][4];
        for (int j = 0; j < 4; j++) {
            for (int k = 0; k < 4; k++)
                block[j][k] = padpt[i * BLOCK_SIZE + j * 4 + k];
        }
        if (i == 0) cbc(block, IV);
        else cbc(block, prev);

        addRoundKey(block, keym);
        for (int j = 0; j < 10; j++) {
            char RoundKey[4][4];
            for (int k = 0; k < 4; k++) {
                for (int l = 0; l < 4; l++)
                    RoundKey[k][l] = expandedKey[j + 4 + k][l];
            }
            SubBytes(block);
            ShiftRows(block);
            if (j < 9) MixColumns(block);
            addRoundKey(block, RoundKey);
        }
        for (int j = 0; j < 4; j++) {
            for (int k = 0; k < 4; k++) {
                prev[j][k] = block[j][k];
            }
        }
        for (int j = 0; j < BLOCK_SIZE; j++)
            enc[i * BLOCK_SIZE + j] = block[j / 4][j % 4];
    }
    free(padpt);

    return ptlen + (16 - (ptlen % 16));
}